# Append Function 

fruits =[]
fruits.append("apple")
fruits.append("mango")
fruits.append("grapes")
fruits.append("banana")
print(fruits)
fruits.insert(2,"guava")  #insert
print(fruits)
fruits.remove("grapes")  #remove
print(fruits)
fruits.reverse()    #reverse
print(fruits)
print(fruits[::-1])
dry_fruits=["almond","cashew","walnut"]
fruits.extend(dry_fruits)
print(fruits)
fruits.sort()  #sorting
print(fruits)

fruits.sort(reverse=True)  #sorting
print(fruits)



